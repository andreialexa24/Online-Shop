DROP SCHEMA IF EXISTS Proiect;
CREATE DATABASE IF NOT EXISTS Proiect;
use Proiect;


CREATE TABLE IF NOT EXISTS Persoana (
    Numar_Cont VARCHAR(8) UNIQUE PRIMARY KEY,
    Nume VARCHAR(30),
    Prenume VARCHAR(30),
    Data_Nasterii DATE
);

CREATE TABLE IF NOT EXISTS Adresa (
    Id_Adresa INT PRIMARY KEY AUTO_INCREMENT,
    Oras VARCHAR(20),
    Strada VARCHAR(40),
    Bloc VARCHAR(10) DEFAULT NULL,
    Apartament INT DEFAULT NULL,
    Numar_Cont VARCHAR(8) UNIQUE,
    FOREIGN KEY (Numar_Cont)
        REFERENCES Persoana (Numar_Cont)
);

CREATE TABLE IF NOT EXISTS Angajat (
    Id_Angajat INT PRIMARY KEY AUTO_INCREMENT,
    Salariu DECIMAL(6 , 2 ),
    Deducere DECIMAL(6 , 2 ),
    Numar_Cont VARCHAR(8),
    Brut DECIMAL(6 , 2 ),
    FOREIGN KEY (Numar_Cont)
        REFERENCES Persoana (Numar_Cont)
);

CREATE TABLE IF NOT EXISTS Client (
    Id_Persoana INT PRIMARY KEY AUTO_INCREMENT,
    Username VARCHAR(30),
    Parola VARCHAR(20),
    Numar_Cont VARCHAR(8),
    FOREIGN KEY (Numar_Cont)
        REFERENCES Persoana (Numar_Cont),
    INDEX (Username)
);

CREATE TABLE IF NOT EXISTS Furnizor (
    Id_Furnizor INT PRIMARY KEY AUTO_INCREMENT,
    Nume VARCHAR(30),
    Tara VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS Produse (
    Id_Produs INT PRIMARY KEY AUTO_INCREMENT,
    Descriere VARCHAR(80),
    Stoc INT CHECK (Stoc <= 200),
    Valoare_Unitara DECIMAL(6 , 2 ),
    Garantie INT CHECK (Garantie <= 5),
    Id_Furnizor INT,
    FOREIGN KEY (Id_Furnizor)
        REFERENCES Furnizor (Id_Furnizor),
    INDEX (Descriere)
);

CREATE TABLE IF NOT EXISTS Comanda (
    Id_Comanda INT PRIMARY KEY AUTO_INCREMENT,
    Id_Produs INT,
    Numar_Cont VARCHAR(8),
    Cantitate INT,
    Data_Comanda DATE,
    FOREIGN KEY (Id_Produs)
        REFERENCES Produse (Id_Produs),
    FOREIGN KEY (Numar_Cont)
        REFERENCES Persoana (Numar_Cont)
);

CREATE TABLE IF NOT EXISTS Review (
    Id_Review INT PRIMARY KEY AUTO_INCREMENT,
    Descriere VARCHAR(100) DEFAULT NULL,
    Stele INT CHECK (Stele <= 5.0),
    Id_Produs INT,
    Numar_Cont VARCHAR(8),
    FOREIGN KEY (Id_Produs)
        REFERENCES Produse (Id_Produs),
    FOREIGN KEY (Numar_Cont)
        REFERENCES Persoana (Numar_Cont)
);



